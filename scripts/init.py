#!/usr/bin/env python3
"""
Crafty — Company Workspace Initializer
Run this once from the workspace root after cloning crafty/.
Creates config.yaml and the full company/ folder scaffold.

Usage:
    python crafty/scripts/init.py
"""

import os
import sys
from datetime import date

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def ask(prompt: str, default: str = "") -> str:
    if default:
        value = input(f"{prompt} [{default}]: ").strip()
        return value if value else default
    value = input(f"{prompt}: ").strip()
    while not value:
        value = input(f"  (required) {prompt}: ").strip()
    return value


def write(path: str, content: str) -> None:
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"  created  {path}")


def current_quarter() -> str:
    today = date.today()
    q = (today.month - 1) // 3 + 1
    return f"{today.year}-Q{q}"


# ---------------------------------------------------------------------------
# Collect inputs
# ---------------------------------------------------------------------------

def collect_inputs() -> dict:
    print("\n=== Crafty — New Company Workspace Init ===\n")

    company_name = ask("Company name")
    owner        = ask("Your handle (default owner)", default="founder")
    repos_root   = ask("Absolute path to source code repos root (e.g. C:/dev/mycompany-repos)")
    quarter      = ask("Current quarter", default=current_quarter())
    today        = date.today().isoformat()

    return {
        "company_name": company_name,
        "owner":        owner,
        "repos_root":   repos_root,
        "quarter":      quarter,
        "today":        today,
    }



# ---------------------------------------------------------------------------
# Scaffold
# ---------------------------------------------------------------------------

def scaffold(root: str, ctx: dict) -> None:
    print(f"\nScaffolding workspace at: {root}\n")

    write(os.path.join(root, "crafty", "config.yaml"),                        config_yaml(ctx))
    write(os.path.join(root, "company", "identity", "vision.md"),              vision_md(ctx))
    write(os.path.join(root, "company", "identity", "principles.md"),          principles_md(ctx))
    write(os.path.join(root, "company", "identity", "business_model.md"),      business_model_md(ctx))
    write(os.path.join(root, "company", "strategy", "okr", f"{ctx['quarter']}.md"), okr_md(ctx))
    write(os.path.join(root, "company", "execution", "initiatives", ".gitkeep"), "")
    write(os.path.join(root, "company", "execution", "projects",    ".gitkeep"), "")
    write(os.path.join(root, "company", "execution", "tasks",       ".gitkeep"), "")
    write(os.path.join(root, "company", "state",     "current_focus.md"),      current_focus_md(ctx))
    write(os.path.join(root, "company", "state",     "backlog.md"),            backlog_md(ctx))
    write(os.path.join(root, "company", "state",     "decisions.md"),          decisions_md(ctx))
    write(os.path.join(root, "company", "logs",      "decisions-log.md"),      decisions_log_md(ctx))
    write(os.path.join(root, "company", "logs",      "weekly-review.md"),      weekly_review_md(ctx))
    write(os.path.join(root, "company", ".github",  "copilot-instructions.md"), copilot_instructions_md(ctx))

    workspace_name = ctx['company_name'].replace(' ', '')
    write(os.path.join(root, f"{workspace_name}.code-workspace"),              code_workspace(ctx))

    print(f"""
Done. Next steps:
  1. Fill in company/identity/ files with your vision, principles, and business model.
  2. Define your first OKR in company/strategy/okr/{ctx['quarter']}.md.
  3. Open {workspace_name}.code-workspace in VS Code and start the agent loop.
""")


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def main() -> None:
    # Resolve workspace root: the folder containing crafty/
    script_dir  = os.path.dirname(os.path.abspath(__file__))          # crafty/scripts/
    crafty_dir  = os.path.dirname(script_dir)                         # crafty/
    root        = os.path.dirname(crafty_dir)                         # workspace root

    ctx = collect_inputs()
    scaffold(root, ctx)


if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n\nAborted.")
        sys.exit(1)
